#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *ps1;
	char *ps2;

	ps1 = s1;
	ps2 = (char*)s2;
	while (*ps1 != '\0')
		ps1++;
	while (*ps2 != '\0')
	{
		*ps1 = *ps2;
		ps1++;
		ps2++;
	}
	*ps1 = *ps2;
	return (s1);
}
