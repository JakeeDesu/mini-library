#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char *ps1;
	char *ps2;

	ps1 = s1;
	ps2 = (char*)s2;
	while (n > 0)
	{
		if (*ps2 != '\0')
		{
			*ps1 = *ps2;
			ps1++;
			ps2++;
		}
		else
		{
			*ps1 = '\0';
			ps1++;
		}
		n--;
	}
	return (s1);
}
