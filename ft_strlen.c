#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	cnt;
	char	*ps;

	cnt = 0;
	ps = (char*)s;
	while (*ps != '\0')
	{
		cnt++;
		ps++;
	}
	return (cnt);
}
